                    <p align="justify"><b>Ask</b> The quoted price at which a customer can buy a currency pair. Also
                        referred to as the 'offer', 'ask price', or 'ask rate'.</p>

                    <p align="justify"><b>Base Currency</b> For foreign exchange trading, currencies are quoted in terms
                        of a currency pair. The first currency in the pair is the base currency. For example, in a
                        USD/JPY currency pair, the US dollar is the base currency. Also may be referred to as the
                        primary currency.</p>

                    <p align="justify"><b>Bid</b> The quoted price where a customer can sell a currency pair. Also known
                        as the 'bid price' or 'bid rate'.</p>

                    <p align="justify"><b>Bid/Ask</b> Spread The point difference between the bid and ask (offer) price.
                    </p>

                    <p align="justify"><b>Call</b> A call option gives the option buyer the right to purchase a
                        particular currency pair at a stated exchange rate.</p>

                    <p align="justify"><b>Counterparty</b> The counterparty is the person who is on the other side of an
                        OTC trade. For retail customers, the dealer will always be the counterparty.</p>

                    <p align="justify"><b>Cross-rate</b> The exchange rate between two currencies where neither of the
                        currencies are the US dollar.</p>

                    <p align="justify"><b>Currency pair</b> The two currencies that make up a foreign exchange rate. For
                        example, USD/YEN is a currency pair.</p>

                    <p align="justify"><b>Dealer</b> A firm in the business of acting as a counterparty to foreign
                        currency transactions.</p>

                    <p align="justify"><b>Euro</b> The common currency adopted by eleven European nations (i.e.,
                        Austria, Belgium, Finland, France, Germany, Ireland, Italy, Luxembourg, the Netherlands,
                        Portugal and Spain) on January 1, 1999.</p>

                    <p align="justify"><b>European-style</b> option An option contract that can be exercised only on or
                        near its expiration date.</p>

                    <p align="justify"><b>Expiration</b> This is the last day on which an option may either be exercised
                        or offset.</p>

                    <p align="justify"><b>Forward transaction</b> A true forward transaction is an agreement that
                        expects actual delivery of and full payment for the currency to occur on a future date. This
                        term may also be used to refer to transactions that the parties expect to offset at some time in
                        the future, but these transactions are not true forward transactions and are governed by the
                        federal Commodity Exchange Act.</p>

                    <p align="justify"><b>Interbank market</b> A loose network of currency transactions negotiated
                        between financial institutions and other large companies.</p>

                    <p align="justify"><b>Leverage</b> The ability to control large dollar amount of a commodity with a
                        comparatively small amount of capital. Also known as 'gearing'.</p>

                    <p align="justify"><b>Margin</b> See Security Deposit.</p>

                    <p align="justify"><b>Offer</b> See ask.</p>

                    <p align="justify"><b>Open position</b> Any transaction that has not been closed out by a
                        corresponding opposite transaction.</p>

                    <p align="justify"><b>Pip</b> The smallest unit of trading in a foreign currency price.</p>

                    <p align="justify"><b>Premium</b> The price an option buyer pays for the option, not including
                        commissions.</p>

                    <p align="justify"><b>Put</b> A put option gives the option buyer the right to sell a particular
                        currency pair at a stated exchange rate.</p>

                    <p align="justify"><b>Quote currency</b> The second currency in a currency pair is referred to as
                        the quote currency. For example, in a USD/JPY currency pair, the Japanese yen is the quote
                        currency. Also referred to as the secondary currency or the counter currency.</p>

                    <p align="justify"><b>Rollover</b> The process of extending the settlement date on an open position
                        by rolling it over to the next settlement date.</p>

                    <p align="justify"><b>Retail customer</b> Any party to a forex trade who is not an eligible contract
                        participant as defined under the Commodity Exchange Act. This includes individuals with assets
                        of less than $10 million and most small businesses.</p>

                    <p align="justify"><b>Security deposit</b> The amount of money needed to open or maintain a
                        position. Also known as 'margin'.</p>

                    <p align="justify"><b>Settlement</b> The actual delivery of currencies made on the maturity date of
                        a trade.</p>

                    <p align="justify"><b>Spot market</b> A market of immediate delivery of and payment for the product,
                        in this case, currency.</p>

                    <p align="justify"><b>Spot transaction</b> A true spot transaction is a transaction requiring prompt
                        delivery of and full payment for the currency. In the interbank market, spot transactions are
                        usually settled in two business days. This term may also be used to refer to transactions that
                        the parties expect to offset or roll over within two business days, but these transactions are
                        not true spot transactions and are governed by the federal Commodity Exchange Act.</p>

                    <p align="justify"><b>Spread</b> The point or pip difference between the ask and bid price of a
                        currency pair.</p>

                    <p align="justify"><b>Sterling</b> Another term for British currency, the pound.</p>

                    <p align="justify"><b>Strike price</b> The exchange rate at which the buyer of a call has the right
                        to purchase a specific currency pair or at which the buyer of a put has the right to sell a
                        specific currency pair. Also known as the 'exercise price'.</p>
